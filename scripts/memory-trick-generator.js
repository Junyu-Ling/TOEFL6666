function defHint(definitions) {
  return definitions.map((d) => d.replace(/^[^.]+\.\s*/, "")).join("；");
}

function findStems(word) {
  const lower = word.toLowerCase().replace(/-/g, "");
  const hits = [];
  for (const [stem, meaning] of Object.entries(STEM_HINTS)) {
    if (lower.includes(stem) && !hits.some((h) => h.stem === stem)) {
      hits.push({ stem, meaning });
    }
  }
  return hits.slice(0, 3);
}

export function buildMemoryTrick(word, definitions) {
  const lower = word.toLowerCase();
  const hint = defHint(definitions);
  const prefixes = ROOT_PATTERNS.filter((p) => p.match(lower)).slice(0, 3);
  const stems = findStems(lower);

  const useRoot = prefixes.length > 0 || stems.length > 0 || word.length > 7;

  if (useRoot) {
    const parts = [
      ...prefixes.map((p) => `${p.part}(${p.meaning})`),
      ...stems.map((s) => `${s.stem}(${s.meaning})`),
    ];
    const formula = parts.length > 0 ? `${parts.join(" + ")} → ${word}` : `${word} → ${hint}`;
    const explain = parts.length > 0 ? `从词形看：${parts.join("，")}。` : "";
    return {
      type: "root",
      formula,
      content: `${explain}整体义：${hint}。`.slice(0, 120),
    };
  }

  const slice = word.slice(0, Math.min(4, word.length));
  return {
    type: "homophone",
    formula: `${word} ≈ 谐音/画面联想`,
    content: `把「${word}」读慢并联想「${hint}」，多读几遍形成条件反射。`.slice(0, 120),
  };
}
